  'MY_EPUBS.GO_TO_FIXER',
  'INSTRUCTIONS',
  'SETTINGS.INSTRUCTIONS',
  'SETTINGS.INSTRUCTIONS_SECTION',
  'SETTINGS.INSTRUCTIONS_HINT',
