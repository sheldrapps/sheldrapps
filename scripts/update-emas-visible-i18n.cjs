function setAtPath(root, keyPath, value) {
  const parts = keyPath.split('.');
  let cursor = root;

  for (let index = 0; index < parts.length - 1; index += 1) {
    const key = parts[index];
    if (!cursor[key] || typeof cursor[key] !== 'object') {
      cursor[key] = {};
    }
    cursor = cursor[key];
  }

  cursor[parts[parts.length - 1]] = value;
}

function deleteAtPath(root, keyPath) {
  const parts = keyPath.split('.');
  let cursor = root;

  for (let index = 0; index < parts.length - 1; index += 1) {
    cursor = cursor?.[parts[index]];
    if (!cursor || typeof cursor !== 'object') {
      return;
    }
  }

  if (cursor && typeof cursor === 'object') {
    delete cursor[parts[parts.length - 1]];
  }
}

function updateLocale(filePath, updates) {
  const data = JSON.parse(fs.readFileSync(filePath, 'utf8').replace(/^\uFEFF/, ''));

  for (const keyPath of removedPaths) {
    deleteAtPath(data, keyPath);
  }

  for (const [key, value] of Object.entries(updates)) {
    setAtPath(data, key, value);
  }

  const serialized = `${JSON.stringify(data, null, 2)}\n`;
  fs.writeFileSync(filePath, serialized, 'utf8');

  for (const mirrorDir of mirrorLocaleDirs) {
    const mirrorPath = path.join(mirrorDir, path.basename(filePath));
    if (fs.existsSync(mirrorPath)) {
      fs.writeFileSync(mirrorPath, serialized, 'utf8');
    }
  }
}

for (const locale of localeOrder) {
  const filePath = path.join(sourceLocaleDir, `${locale}.json`);
  const updates = updatesByLocale[locale];

  if (!updates || !fs.existsSync(filePath)) {
    continue;
  }

  updateLocale(filePath, updates);
  console.log(`updated ${locale}`);
}
